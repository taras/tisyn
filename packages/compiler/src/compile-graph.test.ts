// ── Symbol extraction and reachability (§16) ──

describe("symbol extraction and reachability", () => {
  it("SE-001: exported function* → entrypoint, compiled", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `export function* process() { return 42; }`,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("process");
  });

  it("SE-002: exported const-bound generator → entrypoint", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `export const process = function*() { return 42; };`,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("process");
  });

  it("SE-003: non-exported helper called from entrypoint → compiled", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          function* helper() { return 1; }
          export function* main() { return yield* helper(); }
        `,
      }),
      validate: false,
    });

    expect(result.graph.compiled.length).toBe(2);
    // helper should have a module-scoped name
    const helperName = result.graph.compiled.find((n) => n !== "main");
    expect(helperName).toBeDefined();
  });

  it("SE-004: cross-module helper → resolved and compiled", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { helper } from "./helpers.ts";
          export function* main() { return yield* helper(); }
        `,
        "/helpers.ts": `
          export function* helper() { return 42; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("main");
    expect(result.graph.compiled).toContain("helper");
  });

  it("SE-005: unreachable exported symbol → not compiled", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          export function* main() { return 1; }
          export function* unused() { return 2; }
        `,
      }),
      validate: false,
    });

    // main is the entrypoint, unused is not reachable
    // Both are exported generators so both are entrypoints
    // Actually both are exported generators so both ARE entrypoints
    expect(result.graph.compiled).toContain("main");
    expect(result.graph.compiled).toContain("unused");
  });

  it("SE-006: no generators → E-GRAPH-001", () => {
    expect(() =>
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `export function helper() { return 1; }`,
        }),
        validate: false,
      }),
    ).toThrow(CompileError);

    try {
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `export function helper() { return 1; }`,
        }),
        validate: false,
      });
    } catch (e) {
      expect((e as CompileError).code).toBe("E-GRAPH-001");
    }
  });

  it("SE-007: transitive reachability A → B", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { a } from "./a.ts";
          export function* main() { return yield* a(); }
        `,
        "/a.ts": `
          import { b } from "./b.ts";
          export function* a() { return yield* b(); }
        `,
        "/b.ts": `
          export function* b() { return 42; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("main");
    expect(result.graph.compiled).toContain("a");
    expect(result.graph.compiled).toContain("b");
  });

  it("SE-009: export { local as exported } → resolves through rename", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { exported } from "./mod.ts";
          export function* main() { return yield* exported(); }
        `,
        "/mod.ts": `
          function* local() { return 42; }
          export { local as exported };
        `,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("main");
    expect(result.graph.compiled).toContain("exported");
  });

  it("SE-010: emission order is deterministic", () => {
    const files = {
      "/root.ts": `
        import { a } from "./a.ts";
        import { b } from "./b.ts";
        export function* main() { yield* a(); return yield* b(); }
      `,
      "/a.ts": `export function* a() { return 1; }`,
      "/b.ts": `export function* b() { return 2; }`,
    };

    const r1 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });
    const r2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });

    expect(r1.graph.compiled).toEqual(r2.graph.compiled);
  });
});

// ── Name conflicts (§19) ──

describe("name conflicts", () => {
  it("NC-001: two modules export same-named generator → E-NAME-001", () => {
    expect(() =>
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `
            import { process } from "./other.ts";
            export function* process() { return yield* process(); }
          `,
          "/other.ts": `
            export function* process() { return 1; }
          `,
        }),
        validate: false,
      }),
    ).toThrow(CompileError);
  });

  it("NC-005: non-exported same-named helpers → distinct names, accepted", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { a } from "./a.ts";
          import { b } from "./b.ts";
          function* helper() { return 0; }
          export function* main() { yield* helper(); yield* a(); return yield* b(); }
        `,
        "/a.ts": `
          function* helper() { return 1; }
          export function* a() { return yield* helper(); }
        `,
        "/b.ts": `
          function* helper() { return 2; }
          export function* b() { return yield* helper(); }
        `,
      }),
      validate: false,
    });

    // Should have three distinct names for the three helpers
    const helperNames = result.graph.compiled.filter(
      (n) => n !== "main" && n !== "a" && n !== "b",
    );
    expect(helperNames.length).toBe(3);
    // All distinct (EN2)
    expect(new Set(helperNames).size).toBe(3);
  });
});

// ── Artifact identity and determinism (§22) ──

describe("artifact identity and determinism", () => {
  it("AI-DET-001: same inputs → byte-identical output (printed)", () => {
    const files = {
      "/root.ts": `
        import { helper } from "./h.ts";
        export function* main() { return yield* helper(); }
      `,
      "/h.ts": `export function* helper() { return 42; }`,
    };

    const r1 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });
    const r2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });

    expect(r1.source).toBe(r2.source);
  });

  it("AI-DET-002: same inputs → byte-identical output (json)", () => {
    const files = {
      "/root.ts": `export function* main() { return 42; }`,
    };

    const r1 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "json" });
    const r2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "json" });

    expect(r1.source).toBe(r2.source);
  });

  it("AI-CHG-001: modified helper body → different output", () => {
    const base = {
      "/root.ts": `
        import { h } from "./h.ts";
        export function* main() { return yield* h(); }
      `,
    };

    const r1 = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({ ...base, "/h.ts": `export function* h() { return 1; }` }),
      validate: false,
    });
    const r2 = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({ ...base, "/h.ts": `export function* h() { return 2; }` }),
      validate: false,
    });

    expect(r1.source).not.toBe(r2.source);
  });

  it("AI-NAME-001: exported symbol uses source name", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `export function* processOrder() { return 1; }`,
      }),
      validate: false,
    });

    expect(result.graph.compiled).toContain("processOrder");
    expect(result.source).toContain("processOrder");
  });

  it("AI-NAME-002: non-exported same-named helpers → distinct emitted names", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { a } from "./a.ts";
          import { b } from "./b.ts";
          export function* main() { yield* a(); return yield* b(); }
        `,
        "/a.ts": `
          function* helper() { return 1; }
          export function* a() { return yield* helper(); }
        `,
        "/b.ts": `
          function* helper() { return 2; }
          export function* b() { return yield* helper(); }
        `,
      }),
      validate: false,
    });

    const helperNames = result.graph.compiled.filter(
      (n) => n !== "main" && n !== "a" && n !== "b",
    );
    expect(helperNames.length).toBe(2);
    expect(helperNames[0]).not.toBe(helperNames[1]);
  });

  it("AI-NAME-003: recompile → identical non-exported names (EN4)", () => {
    const files = {
      "/root.ts": `
        import { a } from "./a.ts";
        function* localHelper() { return 0; }
        export function* main() { yield* localHelper(); return yield* a(); }
      `,
      "/a.ts": `
        function* helper() { return 1; }
        export function* a() { return yield* helper(); }
      `,
    };

    const r1 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });
    const r2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false });

    expect(r1.graph.compiled).toEqual(r2.graph.compiled);
  });

  it("AI-FMT-001: printed vs json → different text, each deterministic", () => {
    const files = {
      "/root.ts": `export function* main() { return 42; }`,
    };

    const printed = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "printed" });
    const json = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "json" });

    expect(printed.source).not.toBe(json.source);
    // Each is internally deterministic
    const p2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "printed" });
    const j2 = compileGraph({ roots: ["/root.ts"], readFile: makeReadFile(files), validate: false, format: "json" });
    expect(printed.source).toBe(p2.source);
    expect(json.source).toBe(j2.source);
  });
});

