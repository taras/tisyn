/**
 * Conformance tests for compileGraph — rooted import-graph compilation.
 *
 * Tests observe only the public result surface:
 * - result.source — emitted artifact text
 * - result.graph.modules — module categories and participation
 * - result.graph.compiled — compiled symbol names
 * - result.graph.traversed — traversed module paths
 * - CompileError — error code and diagnostic content
 */

import { describe, it, expect } from "vitest";
import { compileGraph, CompileError } from "./index.js";

function makeReadFile(files: Record<string, string>): (path: string) => string {
  return (path) => {
    if (!(path in files)) { throw new Error(`ENOENT: ${path}`); }
    return files[path]!;
  };
}

// ── Graph construction (§14) ──

describe("module graph construction", () => {
  it("IG-001: single root, no imports", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `export function* main() { return 1; }`,
      }),
      validate: false,
    });

    expect(result.graph.modules["/root.ts"]?.category).toBe("workflow-implementation");
    expect(result.graph.traversed).toEqual(["/root.ts"]);
    expect(result.graph.compiled).toContain("main");
  });

  it("IG-002: root imports helper", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { helper } from "./helper.ts";
          export function* main() { return yield* helper(); }
        `,
        "/helper.ts": `
          export function* helper() { return 42; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.traversed).toContain("/root.ts");
    expect(result.graph.traversed).toContain("/helper.ts");
    expect(result.graph.compiled).toContain("main");
    expect(result.graph.compiled).toContain("helper");
  });

  it("IG-003: import without extension → E-IMPORT-002", () => {
    expect(() =>
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `
            import { helper } from "./helper";
            export function* main() { return yield* helper(); }
          `,
        }),
        validate: false,
      }),
    ).toThrow(CompileError);

    try {
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `import { helper } from "./helper"; export function* main() { return 1; }`,
        }),
        validate: false,
      });
    } catch (e) {
      expect((e as CompileError).code).toBe("E-IMPORT-002");
    }
  });

  it("IG-004: bare specifier import → external boundary, not traversed", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { something } from "lodash";
          export function* main() { return 1; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.traversed).toEqual(["/root.ts"]);
  });

  it("IG-005: node: protocol import → external boundary", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { readFile } from "node:fs";
          export function* main() { return 1; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.traversed).toEqual(["/root.ts"]);
  });

  it("IG-006: generatedModulePaths → generated classification", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { dep } from "./generated.ts";
          export function* main() { return 1; }
        `,
        "/generated.ts": `export const dep = 42;`,
      }),
      generatedModulePaths: ["/generated.ts"],
      validate: false,
    });

    expect(result.graph.modules["/generated.ts"]?.category).toBe("generated");
  });

  it("IG-007: auto-generated banner → generated via heuristic", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { dep } from "./prior.ts";
          export function* main() { return 1; }
        `,
        "/prior.ts": `// Auto-generated by @tisyn/compiler — do not edit\nexport const dep = 42;`,
      }),
      validate: false,
    });

    expect(result.graph.modules["/prior.ts"]?.category).toBe("generated");
  });

  it("IG-008: type-only import → not traversed, forwarded", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import type { MyType } from "./types.ts";
          export function* main() { return 1; }
        `,
      }),
      validate: false,
    });

    // Type-only imports are NOT traversed (no readFile for types.ts)
    expect(result.graph.traversed).toEqual(["/root.ts"]);
    // Type import should be forwarded in artifact
    expect(result.source).toContain("import type");
  });

  it("IG-009: import missing file → E-IMPORT-003", () => {
    expect(() =>
      compileGraph({
        roots: ["/root.ts"],
        readFile: makeReadFile({
          "/root.ts": `
            import { helper } from "./missing.ts";
            export function* main() { return 1; }
          `,
        }),
        validate: false,
      }),
    ).toThrow(CompileError);
  });

  it("IG-010: chain root → A → B → C", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { a } from "./a.ts";
          export function* main() { return yield* a(); }
        `,
        "/a.ts": `
          import { b } from "./b.ts";
          export function* a() { return yield* b(); }
        `,
        "/b.ts": `
          import { c } from "./c.ts";
          export function* b() { return yield* c(); }
        `,
        "/c.ts": `
          export function* c() { return 42; }
        `,
      }),
      validate: false,
    });

    expect(result.graph.traversed).toContain("/a.ts");
    expect(result.graph.traversed).toContain("/b.ts");
    expect(result.graph.traversed).toContain("/c.ts");
  });

  it("IG-011: circular import → no error", () => {
    const result = compileGraph({
      roots: ["/root.ts"],
      readFile: makeReadFile({
        "/root.ts": `
          import { a } from "./a.ts";
          export function* main() { return yield* a(); }
        `,
        "/a.ts": `
          import { b } from "./b.ts";
          export function* a() { return yield* b(); }
        `,
        "/b.ts": `
          import { a } from "./a.ts";
          export function* b() { return yield* a(); }
        `,
      }),
      validate: false,
    });

    expect(result.graph.traversed).toContain("/a.ts");
    expect(result.graph.traversed).toContain("/b.ts");
  });
});

